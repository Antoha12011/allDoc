//
var number = 1
while number <= 20 { // До тех пора переменная number меньше или ровна 20 выполнять код снизу
    print(number)    // Распичатать текущее значение и прибавить 1
    number += 1
}
print("Start")
//
//
//
//--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
//
//
//
repeat {
    print(number)
    number += 1
} while number <= 20
print("Start")
// Чтобы выйти из цикла раньше нужно использовать break
//
//
var num = 10
while num >= 0 {    // Пока num больше или ровна 0 делаем print num и удаляем 1
    print(num)
    if num == 4 {   // Внутри цикла while дополнительное условие, когда num = 4 выходим из цикла и выводим "Go"
        print("Go")
        break
    }
    num -= 1
}


